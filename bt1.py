# Nhập vào một chuỗi
chuoi = input("Nhập vào một chuỗi: ")

# Xuất ra chuỗi viết hoa toàn bộ
print("Chuỗi viết hoa toàn bộ:", chuoi.upper())

# Xuất ra chuỗi có ký tự đầu viết hoa (capitalize)
print("Chuỗi viết hoa ký tự đầu:", chuoi.capitalize())

# Xuất ra chuỗi viết thường toàn bộ
print("Chuỗi viết thường toàn bộ:", chuoi.lower())
